// Helper to generate random sparkline data with a decay pattern
function decaySparkline(peak: number, days: number = 90): number[] {
  const data: number[] = []
  const peakDay = Math.floor(Math.random() * 30) + 10
  for (let i = 0; i < days; i++) {
    if (i < peakDay) {
      data.push(Math.floor(peak * (0.3 + 0.7 * (i / peakDay)) + Math.random() * peak * 0.1))
    } else {
      const decay = Math.exp(-0.03 * (i - peakDay))
      data.push(Math.floor(peak * decay * (0.8 + Math.random() * 0.4)))
    }
  }
  return data
}

function stableSparkline(base: number, days: number = 90): number[] {
  return Array.from({ length: days }, () => Math.floor(base * (0.8 + Math.random() * 0.4)))
}

